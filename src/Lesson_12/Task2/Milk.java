package Lesson_12.Task2;

public  class Milk {
    private float size;
    private int priceOfOneLiterOfMilk;

    public Milk(float size, int priceOfOneLiterOfMilk) {
        this.size = size;
        this.priceOfOneLiterOfMilk = priceOfOneLiterOfMilk;
    }

    public float getSize() {
        return size;
    }

    public int getPriceOfOneLiterOfMilk() {
        return priceOfOneLiterOfMilk;
    }


}
