package LessonTwelve.Task2;

public  class Piece {
    private double weightOfOneIceCream;
    private int priceIceCream;

    public Piece(double weightOfOneIceCream, int priceIceCream) {
        this.weightOfOneIceCream = weightOfOneIceCream;
        this.priceIceCream = priceIceCream;
    }

    public double getWeightOfOneIceCream() {
        return weightOfOneIceCream;
    }

    public int getPriceIceCream() {
        return priceIceCream;
    }




}
