package LessonTwelve.Task2;

public    class Suger  {
    private float weight;
    private int thePriceOfOneKgsOfSugar;

    public Suger(float weight, int thePriceOfOneKgsOfSugar) {
        this.weight = weight;
        this.thePriceOfOneKgsOfSugar = thePriceOfOneKgsOfSugar;
    }

    public float getWeight() {
        return weight;
    }

    public int getThePriceOfOneKgsOfSugar() {
        return thePriceOfOneKgsOfSugar;
    }


}
